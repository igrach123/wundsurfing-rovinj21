                <p class="text-muted text-justify">
                    Na samo 4,5 sata leta od Europe smjestio se otok Tenerife,otok s toplim oceanom i zrakom čak i u
                    zimskim mjesecima, prekrasnim pješćanim plažama, vjetrom i valovima. Upravo na tom otoku, u
                    gradiću El Medano koji je raj za surfere iz cijeloga svijeta nalazimo se mi, od listopada pa sve do
                    ožujka.
                    <br> <br>
                    <b>Tenerife</b> je otok u Atlanskom oceanu te je dio Kanarskog otočja. Krasi ga raznolika priroda,
                    od zelenih
                    šuma Eukaliptusa i plantaža banana do suhe afričke strane na kojoj se nalaze pješćane plaže.
                    Otok je poznat i po vulkanu El Teide koji je i najviši vrh Španjolske 3217 m.n.v.
                    Sa istočne strane otoka Tenerife tokom cijele godine puše topao vjetar iz Afrike (NE) koji u ljetnim
                    mjesecima puše od 25-35 čvorova, a u zimskim mjesecima je nešto slabiji, između 15-30 čvorova.
                    Taj vjetar u gradiću El Medano stvara idealne uvjete za windsurf od početnika pa do profesionalnih
                    wave ridera.
                    Tokom zimskih mjeseci sa nama u El Medanu možete naučit windsurf, nudimo tečajeve od početnog
                    pa do wave riding tečajeva i to sve sa najnovijom opremom brendova Simmer, RRD, Goya, Point 7 u
                    suradnji sa lokalnim windsurf centrom Ocean Calling. Na raspolaganju imamo preko 30 dasaka od
                    početničkih pa do pro wave dasaka i preko 50 jedara različitih veličina.
                    Tečajevi koje radimo su isključivo individualni, popračeni video analizom te se na kraju svakog dana
                    pregledom videa pokušavaju ispraviti i najsitnije greške. <br> <br>
                    <b>Dođite i pridružite nam se, bilo da ste iskusni ili tek znatiželjni, odgovorite pozivu
                        vjetra.</b>
